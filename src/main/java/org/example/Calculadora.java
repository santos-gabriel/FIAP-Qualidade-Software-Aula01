package org.example;

public class Calculadora {

    public int somar(int valueA, int valueB) {
        return valueA + valueB;
    }

    public int subtrait(int valueA, int valueB) {
        return valueA - valueB;
    }

    public int multiplicar(int valueA, int valueB) {
        return valueA * valueB;
    }

    public int dividir(int valueA, int valueB) {
        return valueA / valueB;
    }

}
